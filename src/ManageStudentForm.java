    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row;
        row = jTable1.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        jTextField_id.setText(dtm.getValueAt(row,0).toString());
        jTextField_firstName.setText(dtm.getValueAt(row,1).toString());
        jTextField_lastName.setText(dtm.getValueAt(row,2).toString());
        if(dtm.getValueAt(row, 3).toString().equals("female")){
            jRadioButton_female.setSelected(true);
            jRadioButton_male.setSelected(false);
            
        }else{
            jRadioButton_female.setSelected(false);
            jRadioButton_male.setSelected(true);
        }
        Date dt;
        
        try {
            dt=new SimpleDateFormat("yyyy-MM-dd").parse(dtm.getValueAt(row, 4).toString());
            jDateChooser_birthdate.setDate(dt);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,"Not Parsed");
        }
        
        
        jTextField_contact.setText(dtm.getValueAt(row,5).toString());
        jTextField_address.setText(dtm.getValueAt(row,6).toString());        
    }//GEN-LAST:event_jTable1MouseClicked

