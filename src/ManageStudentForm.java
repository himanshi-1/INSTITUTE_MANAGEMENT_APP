    public boolean verifyText(){
        if(jTextField_firstName.getText().equals("") || jTextField_lastName.getText().equals("") || 
                jTextField_contact.getText().equals("") || jTextField_address.getText().equals("") || jTextField_id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "One or more Empty Field.");
            return false;
        }else if(jDateChooser_birthdate.getDate().compareTo(new Date())>0){
            JOptionPane.showMessageDialog(null, "No Future Student are Allowed.");
            return  false;
        }else{
           return true; 
        }
        
    }
